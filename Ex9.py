print("Affichage de 8 jusqu'a 16 par une boucle for")
for k in range(8,17): # Range(n,m)  output : n jusqu'a m-1
    print(k)
print("affichage d'une liste des nombres qui part par 5 jusqu'à 22 avec un pas égale 4")
